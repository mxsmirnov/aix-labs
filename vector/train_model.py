from gensim.models import Word2Vec
import random
import os

print("=" * 70)
print("WORD2VEC: ОБУЧЕНИЕ МОДЕЛИ")
print("=" * 70)

# -----------------------------------------------------------------------------
# 1. ПОДГОТОВКА ДАННЫХ
# -----------------------------------------------------------------------------
# ВАЖНО: Все слова в именительном падеже (существительные) 
# и инфинитиве (глаговы). Никаких склонений!

sentences = [
    # Города и страны (все в именительном падеже)
    ["париж", "столица", "франция"],
    ["берлин", "столица", "германия"],
    ["москва", "столица", "россия"],
    ["лондон", "столица", "англия"],
    ["рим", "столица", "италия"],
    ["мадрид", "столица", "испания"],
    ["франция", "страна", "в", "европа"],
    ["германия", "страна", "в", "европа"],
    ["россия", "страна", "в", "европа"],
    ["англия", "страна", "в", "европа"],
    ["италия", "страна", "в", "европа"],
    ["испания", "страна", "в", "европа"],
    
    # Животные (все в именительном падеже)
    ["кот", "домашнее", "животное"],
    ["кошка", "домашнее", "животное"],
    ["собака", "домашнее", "животное"],
    ["пес", "домашнее", "животное"],
    ["кот", "ловить", "мышь"],
    ["кошка", "ловить", "мышь"],
    ["собака", "охранять", "дом"],
    ["кот", "мурлыкать"],
    ["кошка", "мурлыкать"],
    ["собака", "лаять"],
    
    # Еда (все в именительном падеже)
    ["яблоко", "фрукт", "вкусный"],
    ["банан", "фрукт", "вкусный"],
    ["апельсин", "фрукт", "вкусный"],
    ["груша", "фрукт", "вкусный"],
    ["хлеб", "еда", "вкусный"],
    ["молоко", "еда", "вкусный"],
    ["сыр", "еда", "вкусный"],
    
    # Профессии (все в именительном падеже, глаголы в инфинитиве)
    ["врач", "лечить", "человек"],
    ["учитель", "учить", "дети"],
    ["повар", "готовить", "еда"],
    ["инженер", "строить", "здание"],
    ["врач", "работать", "в", "больница"],
    ["учитель", "работать", "в", "школа"],
    ["повар", "работать", "в", "ресторан"],
    
    # Транспорт (все в именительном падеже)
    ["машина", "ездить", "по", "дорога"],
    ["автобус", "ездить", "по", "дорога"],
    ["поезд", "ездить", "по", "рельсы"],
    ["самолет", "лететь", "в", "небо"],
    ["корабль", "плыть", "в", "море"],
]

# Увеличиваем датасет для лучшего обучения
print("\n📊 Расширение обучающей выборки...")
expanded_sentences = []
for i in range(100):
    for sent in sentences:
        variation = sent.copy()
        if random.random() > 0.5:
            variation.insert(0, "очень")
        if random.random() > 0.5:
            variation.append("сегодня")
        expanded_sentences.append(variation)

print(f"✅ Всего предложений для обучения: {len(expanded_sentences)}")

# -----------------------------------------------------------------------------
# 2. ОБУЧЕНИЕ МОДЕЛИ
# -----------------------------------------------------------------------------
print("\n🔄 Обучение модели Word2Vec (это может занять время)...")

model = Word2Vec(
    sentences=expanded_sentences,
    vector_size=100,      # Размерность вектора
    window=5,             # Размер контекстного окна
    min_count=1,          # Минимальная частота слова
    workers=4,            # Количество потоков
    sg=1,                 # 1 = Skip-gram (лучше качество)
    epochs=300,           # Количество проходов по данным
    negative=15,          # Количество негативных примеров
    alpha=0.025,          # Скорость обучения
)

print("✅ Модель успешно обучена!")

# -----------------------------------------------------------------------------
# 3. СОХРАНЕНИЕ МОДЕЛИ
# -----------------------------------------------------------------------------
print("\n💾 Сохранение модели...")

# Создаём папку для моделей, если нет
os.makedirs("models", exist_ok=True)

# Вариант 1: Сохранить полную модель (можно дообучать)
model.save("models/word2vec_full.model")
print("✅ Полная модель сохранена: models/word2vec_full.model")

# Вариант 2: Сохранить только векторы (меньше размер, нельзя дообучать)
model.wv.save("models/word2vec_vectors.kv")
print("✅ Векторы сохранены: models/word2vec_vectors.kv")

# -----------------------------------------------------------------------------
# 4. ПРОВЕРКА ПОСЛЕ ОБУЧЕНИЯ
# -----------------------------------------------------------------------------
print("\n🧪 Быстрая проверка модели...")

words = model.wv

# Проверяем, что слова в правильной форме
test_words = ["франция", "германия", "россия", "кот", "кошка", "врач"]
print("\nПроверка наличия слов в словаре:")
for word in test_words:
    if word in words:
        print(f"  ✅ '{word}' — есть в словаре")
    else:
        print(f"  ❌ '{word}' — НЕТ в словаре")

print("\n" + "=" * 70)
print("ОБУЧЕНИЕ ЗАВЕРШЕНО!")
print("Теперь запустите: python use_model.py")
print("=" * 70)